package bootstrap.liftweb

import net.liftweb._
import util._
import Helpers._

import common._
import http._
import js.jquery.JQueryArtifacts
import sitemap._
import Loc._
import mapper._

import net.liftmodules.JQueryModule
import org.squeryl.PrimitiveTypeMode._
import com.dbschools.mgb.Db


/**
 * A class that's instantiated early and run.  It allows the application
 * to modify lift's environment
 */
class Boot {
  def boot {
    if (!DB.jndiJdbcConnAvailable_?) {
      val vendor = 
	new StandardDBVendor(Props.get("db.driver") openOr "org.h2.Driver",
			     Props.get("db.url") openOr 
			     "jdbc:h2:lift_proto.db;AUTO_SERVER=TRUE",
			     Props.get("db.user"), Props.get("db.password"))

      LiftRules.unloadHooks.append(vendor.closeAllConnections_! _)

      DB.defineConnectionManager(DefaultConnectionIdentifier, vendor)
    }

    // where to search snippet
    LiftRules.addToPackages("com.dbschools.mgb")

    // Build SiteMap
    def sitemap = SiteMap(
      Menu.i("Home") / "index"
        )

    LiftRules.setSiteMap(sitemap)

    //Init the jQuery module, see http://liftweb.net/jquery for more information.
    LiftRules.jsArtifacts = JQueryArtifacts
    JQueryModule.InitParam.JQuery=JQueryModule.JQuery172
    JQueryModule.init()

    //Show the spinny image when an Ajax call starts
    LiftRules.ajaxStart =
      Full(() => LiftRules.jsArtifacts.show("ajax-loader").cmd)
    
    // Make the spinny image go away when it ends
    LiftRules.ajaxEnd =
      Full(() => LiftRules.jsArtifacts.hide("ajax-loader").cmd)

    // Force the request to be UTF-8
    LiftRules.early.append(_.setCharacterEncoding("UTF-8"))

    // What is the function to test if a user is logged in?
    LiftRules.loggedInTest = Full(() => true)

    // Use HTML5 for rendering
    LiftRules.htmlProperties.default.set((r: Req) =>
      new Html5Properties(r.userAgent))

    // Configure notice fade-out in order to not pollute page with notifications
    LiftRules.noticesAutoFadeOut.default.set((notices: NoticeType.Value) ⇒ {
      notices match {
        case NoticeType.Notice  ⇒ Full((4 seconds, 2 seconds))
        case NoticeType.Warning ⇒ Full((6 seconds, 2 seconds))
        case NoticeType.Error   ⇒ Full((8 seconds, 2 seconds))
        case _                  ⇒ Empty
      }
    })

    /* set custom 404 handler */
    LiftRules.uriNotFound.prepend(NamedPF("404handler") {
      case (req, failure) ⇒
        NotFoundAsTemplate(ParsePath(List("404"), "html", false, false))
    })

    Db.initialize()
    S.addAround(new LoanWrapper{override def apply[T](f: => T): T = {inTransaction {f}}})
  }
}
